"""
test_routing_table_validation.py: stdlib unittest boundary suite for
routing-table-validation.py (opt-in PreToolUse hook).

Run:  python hooks/disabled/test_routing_table_validation.py
Pass: all tests exit 0, no crash.

Strategy: each test launches the hook as a subprocess, feeds a JSON payload
on stdin, captures stdout, and asserts deny vs allow.

  DENY  -> stdout contains permissionDecision:deny
  ALLOW -> stdout is empty (or whitespace only)
"""

import io
import json
import os
import subprocess
import sys
import unittest

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

_HOOK = os.path.join(os.path.dirname(os.path.abspath(__file__)), "routing-table-validation.py")
_PYTHON = sys.executable

# Repo root: two levels up from disabled/ (disabled/ -> hooks/ -> repo root)
_REPO = os.path.normpath(os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", ".."))
# Registry lives at <repo>/.claude/registry.json (generated by scripts/generate_registry.py)
_REGISTRY = os.path.join(_REPO, ".claude", "registry.json")


def _run_hook(payload: dict) -> tuple[str, int]:
    """Run the hook subprocess, return (stdout, returncode)."""
    result = subprocess.run(
        [_PYTHON, _HOOK],
        input=json.dumps(payload),
        capture_output=True,
        text=True,
        encoding="utf-8",
    )
    return result.stdout, result.returncode


def _skill_path(name: str = "process-qa") -> str:
    """Return a plausible SKILL.md path for the given skill name."""
    return os.path.join(_REPO, ".claude", "skills", name, "SKILL.md").replace("\\", "/")


def _claude_md_path() -> str:
    return os.path.join(_REPO, "CLAUDE.md").replace("\\", "/")


def _non_routing_path() -> str:
    return os.path.join(_REPO, "work", "note.md").replace("\\", "/")


def _is_deny(stdout: str) -> bool:
    if not stdout.strip():
        return False
    try:
        obj = json.loads(stdout.strip())
        return (
            obj.get("hookSpecificOutput", {}).get("permissionDecision") == "deny"
        )
    except Exception:
        return False


def _is_allow(stdout: str) -> bool:
    return not stdout.strip()


def _deny_names(stdout: str) -> list[str]:
    """Extract the agent names mentioned in the deny reason, for targeted assertions."""
    try:
        obj = json.loads(stdout.strip())
        reason = obj.get("hookSpecificOutput", {}).get("permissionDecisionReason", "")
        import re
        return re.findall(r'"([a-z][a-z0-9_-]+)"', reason)
    except Exception:
        return []


# ---------------------------------------------------------------------------
# Load a real agent from registry for ALLOW tests
# ---------------------------------------------------------------------------

def _real_agent_from_registry() -> str:
    """Return one agent name guaranteed to be in registry.json."""
    try:
        with open(_REGISTRY, "r", encoding="utf-8") as fh:
            data = json.load(fh)
        agents = data.get("agents", {})
        if isinstance(agents, dict):
            # prefer a kebab-named one for shape clarity
            for name in agents:
                if "-" in name and name == name.lower():
                    return name.lower()
            return list(agents.keys())[0].lower()
    except Exception:
        pass
    return "architect-reviewer"  # known present


_REAL_AGENT = _real_agent_from_registry()


