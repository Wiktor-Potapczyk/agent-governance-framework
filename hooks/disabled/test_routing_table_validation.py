class TestDenyProtocolShape(unittest.TestCase):
    """Verify the deny output has the exact hookSpecificOutput shape required by Claude Code."""

    def test_deny_output_protocol_shape(self):
        """
        Deny output must match exactly:
        {"hookSpecificOutput": {"hookEventName": "PreToolUse",
                                "permissionDecision": "deny",
                                "permissionDecisionReason": "..."}}
        Must NOT use the SubagentStop protocol {"decision": "block", ...}.
        """
        payload = {
            "tool_name": "Edit",
            "tool_input": {
                "file_path": _skill_path("process-qa"),
                "old_string": "OLD",
                "new_string": "MUST DISPATCH: totally-ghost-agent-xyz",
            },
        }
        stdout, rc = _run_hook(payload)
        self.assertEqual(rc, 0)
        self.assertTrue(stdout.strip(), "Expected non-empty deny output")

        obj = json.loads(stdout.strip())
        self.assertIn("hookSpecificOutput", obj,
                      "Top-level key must be 'hookSpecificOutput'")
        hso = obj["hookSpecificOutput"]
        self.assertEqual(hso.get("hookEventName"), "PreToolUse")
        self.assertEqual(hso.get("permissionDecision"), "deny")
        self.assertIsInstance(hso.get("permissionDecisionReason"), str)
        self.assertTrue(hso["permissionDecisionReason"].strip())

        # Must NOT use the wrong protocol
        self.assertNotIn("decision", obj,
                         "Must NOT use {'decision':'block'} SubagentStop protocol at top level")

    def test_deny_names_ghost_in_reason(self):
        """
        Deny reason must name the specific unresolved token, not a generic message.
        """
        ghost = "utterly-fake-agent-2026"
        payload = {
            "tool_name": "Edit",
            "tool_input": {
                "file_path": _claude_md_path(),
                "old_string": "OLD",
                "new_string": f"MUST DISPATCH: {ghost}",
            },
        }
        stdout, rc = _run_hook(payload)
        self.assertEqual(rc, 0)
        self.assertTrue(_is_deny(stdout))
        reason = json.loads(stdout)["hookSpecificOutput"]["permissionDecisionReason"]
        self.assertIn(ghost, reason,
                      f"Deny reason must name the unresolved token '{ghost}'")


